RSpec.describe "GalaxyMapTest", type: :request do

  describe 'builds a set of galaxies' do
    let(:gm) { GalaxyMap.new('./spec/test_input/day11.txt') }

    it 'creates galaxies correctly' do
      expect(gm.galaxies.size).to eq(9)
      expect(gm.galaxies.map(&:x)).to eq([4, 9, 0, 8, 1, 12, 9, 0, 5])
      expect(gm.galaxies.map(&:y)).to eq([0, 1, 2, 5, 6, 7, 10, 11, 11])
    end

    it 'calculates shortest path correctly' do
      expect(gm.galaxies[6] - gm.galaxies[0]).to eq(15)
      expect(gm.galaxies[5] - gm.galaxies[2]).to eq(17)
      expect(gm.galaxies[8] - gm.galaxies[4]).to eq(9)
      expect(gm.galaxies[8] - gm.galaxies[7]).to eq(5)
    end

    it 'calculates shortest path sum correctly' do
      expect(gm.galaxies.combination(2).size).to eq(36)
      expect(gm.shortest_path_sum).to eq(374)
    end
  end

  describe 'Part 1' do
    let(:gm) { GalaxyMap.new }

    it 'calculates shortest path sum correctly' do
      expect(gm.galaxies.combination(2).size).to eq((1..429).reduce(:*) / ((1..2).reduce(:*) * (1..(429-2)).reduce(:*)))
      expect(gm.shortest_path_sum).to be > 7397146
    end
  end

